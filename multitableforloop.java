import java.io.*;
public class multitableforloop
{
public static void main(String[] args)
{
System.out.println("*|123456789");
System.out.println("-|---------");
for(int i=1;i<=9;i++){
System.out.println(i+"| "+ i*1+" " + i*2+" " + i*3+" " + i*4+" " + i*5+" " + i*6+" " + i*7+" " + i*8+" " + i*9 );
}
System.out.println(" ");
}
}
